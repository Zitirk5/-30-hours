import java.util.Scanner;
public class Do
{
    public static void main(String[] args)
    {
      Scanner input=new Scanner(System.in);
      int a=input.nextInt(); 
      int i=1;
        
        
        do{
            System.out.println(i);
            i++;
        }
        while(i<a);
        {
            System.out.println(i);
            
        }
        }
    }
        